            <p><span class="titulo-clausula">CLÁUSULA TERCEIRA - DENOMINAÇÃO SOCIAL</span><br />
                A SCP girará sob a denominação social <b><u>RPC E ASSOCIADOS S/S - SCP</u></b>.
